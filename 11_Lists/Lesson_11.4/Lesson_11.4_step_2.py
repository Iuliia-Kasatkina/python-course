# 11.4 Вывод элементов списка
"""
Дополните приведенный код, так чтобы он вывел сумму квадратов элементов списка numbers.

Исходный код:

numbers = [1, 78, 23, -65, 99, 9089, 34, -32, 0, -67, 1, 11, 111]


"""

numbers = [1, 78, 23, -65, 99, 9089, 34, -32, 0, -67, 1, 11, 111]
total = 0

for num in numbers:
    total += num ** 2

print(total)