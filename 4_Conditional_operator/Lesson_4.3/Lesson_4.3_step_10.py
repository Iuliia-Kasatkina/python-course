x = int(input())

if 0 <= x <= 36:
    if x == 0:
        print("–∑–µ–ª–µ–Ω—ã–π")
    else:
        if ((1 <= x <= 10 or 19 <= x <= 28) and x % 2 == 1) or ((11 <= x <= 18 or 29 <= x <= 36) and x % 2 == 0):
            print("–∫—Ä–∞—Å–Ω—ã–π")
        else:
            print("—á–µ—Ä–Ω—ã–π")
else:
    print("–æ—à–∏–±–∫–∞ –≤–≤–æ–¥–∞")