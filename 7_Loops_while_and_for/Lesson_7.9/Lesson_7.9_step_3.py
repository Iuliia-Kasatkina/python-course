"""
–î–µ–ª–∏—Ç–µ–ª–∏-1 üå∂Ô∏è
–ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—é—Ç—Å—è –¥–≤–∞ –Ω–∞—Ç—É—Ä–∞–ª—å–Ω—ã—Ö —á–∏—Å–ª–∞ a –∏ b (a< b). –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –Ω–∞—Ö–æ–¥–∏—Ç –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ –∏–∑ –æ—Ç—Ä–µ–∑–∫–∞ [a;b] (–æ—Ç a –¥–æ b –≤–∫–ª—é—á–∏—Ç–µ–ª—å–Ω–æ) —Å –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–π —Å—É–º–º–æ–π –¥–µ–ª–∏—Ç–µ–ª–µ–π. –ï—Å–ª–∏ —á–∏—Å–µ–ª —Å –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–π —Å—É–º–º–æ–π –¥–µ–ª–∏—Ç–µ–ª–µ–π –Ω–µ—Å–∫–æ–ª—å–∫–æ, —Ç–æ –∏—Å–∫–æ–º—ã–º —á–∏—Å–ª–æ–º —è–≤–ª—è–µ—Ç—Å—è –Ω–∞–∏–±–æ–ª—å—à–µ–µ –∏–∑ –Ω–∏—Ö. –í–∞—à–∞ –ø—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–æ–¥–∏—Ç—å –æ—Ç–≤–µ—Ç –≤ —Å–ª–µ–¥—É—é—â–µ–º —Ñ–æ—Ä–º–∞—Ç–µ:

<—á–∏—Å–ª–æ —Å –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–π —Å—É–º–º–æ–π –¥–µ–ª–∏—Ç–µ–ª–µ–π> <—Å—É–º–º–∞ –¥–µ–ª–∏—Ç–µ–ª–µ–π —ç—Ç–æ–≥–æ —á–∏—Å–ª–∞

–§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
–ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—é—Ç—Å—è –¥–≤–∞ —á–∏—Å–ª–∞, –∫–∞–∂–¥–æ–µ –Ω–∞ –æ—Ç–¥–µ–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–µ.

–§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
–ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ –¥–≤–∞ —á–∏—Å–ª–∞ –Ω–∞ –æ–¥–Ω–æ–π —Å—Ç—Ä–æ–∫–µ, —Ä–∞–∑–¥–µ–ª–µ–Ω–Ω—ã—Ö –ø—Ä–æ–±–µ–ª–æ–º: —á–∏—Å–ª–æ —Å –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–π —Å—É–º–º–æ–π –¥–µ–ª–∏—Ç–µ–ª–µ–π –∏ —Å—É–º–º—É –µ–≥–æ –¥–µ–ª–∏—Ç–µ–ª–µ–π.

"""

a, b = int(input()), int(input())
max_del1 = 0
max_del2 = 0
total1 = 0
total2 = 0

for i in range(a, b+1):
    for j in range(1, i+1):
        if i % j == 0:
            total1 += j
        if total1 >= total2:
            total2 = total1
            max_del1 = i
        if max_del1 > max_del2:
            max_del2 = max_del1
    total1 = 0
    max_del1 = 0

print(max_del2, total2)
